	public static void findInteger(int[][] grid, int findNum) {
		
		int row = 0;
		int col = 0;
		int maxCol = cols - 1;
		int maxRow = rows - 1;
		boolean exists = false;
		boolean check = true;
		int checkCount = 0;
		
		while (check == true && exists == false && row <= maxRow) {
			for (col = 0; col <= maxCol; col++) {
				checkCount++;
				System.out.println("Check: " + checkCount + " -> Checking Row: " + row + ", Col: " + col + " (" + grid[row][col] + ")");
				if (grid[row][col] < findNum) {
					maxCol = col - 1;
				}
				if (grid[row][col] == findNum) {
					exists = true;
					System.out.println("Check: " + checkCount + " -> Integer " + findNum + " exists in Row: " + row + ", Col: " + col);
					break;
				}
			}
			row++;
		}
		if (exists == false) {
			System.out.println("Integer does not exist.");			
		}
	}

